#include "libft.h"

void	ft_putnbr(int n)
{
	long i;

	i = n;
	if (i < 0)
	{
		i = (-1 * i);
		ft_putchar('-');
	}
	if (i > 9)
	{
		ft_putnbr(i / 10);
		ft_putchar((i % 10) + '0');
	}
	else
		ft_putchar(i + '0');
}
