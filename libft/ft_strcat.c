#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int y;

	i = 0;
	y = 0;
	while (s1[i])
		i++;
	while (s2[y])
	{
		s1[i] = s2[y];
		i++;
		y++;
	}
	s1[i] = '\0';
	return (s1);
}
